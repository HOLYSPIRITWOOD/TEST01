from 正则表达式 import 正则匹配
import re

#案例一:匹配账号 只能有 字母和数字组成 , 长度限制 6-10
规则 = "^[a-zA-Z0-9]{6,10}$"
账号1 = "baasdfdfsdfsdfa"
账号2 = "1234sd"
正则匹配(re.findall,规则,账号1) #不匹配
正则匹配(re.findall,规则,账号2) #匹配

#案例2 匹配 qq号 要求第一位不为零 , 长度5-11
规则 = "^[1-9][0-9]{4,10}$"
qq号1 = "01234312"
qq号2 = '343123435'
正则匹配(re.findall,规则,qq号1)  #不匹配
正则匹配(re.findall,规则,qq号2)  #匹配